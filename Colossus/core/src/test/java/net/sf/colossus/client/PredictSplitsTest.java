/** Test helper */
class AllPredictSplits extends ArrayList<PredictSplits>
{
    PredictSplitNode getLeaf(String markerId)
    {
        for (PredictSplits ps : this)
        {
            PredictSplitNode leaf = ps.getLeaf(markerId);
            if (leaf != null)
            {
                return leaf;
            }
        }
        return null;
    }

    void printLeaves()
    {
        for (PredictSplits ps : this)
        {
            ps.printLeaves();
        }
    }

    void printNodes()
    {
        for (PredictSplits ps : this)
        {
            ps.printNodes();
        }
    }
}


